    FROM   all_tables
    WHERE  owner IN ('MUSARK_AUTH', 'MUSARK_STORAGE', 'MUSARK_ACTOR', 'MUSIT_MAPPING'))
  LOOP
    EXECUTE IMMEDIATE 'GRANT ALL ON '||x.ownertab||' TO MUSIT';
  END LOOP;
END;

